for(1..length){
if($b[$.][$_]eq"w"){
    $l{"$. $_"} = $l{"@{[$.-1,$_]}"} if $b[ $. - 1 ][$_] eq"w";
    $l{"$. $_"} = $l{"@{[$.,$_-1]}"} if $b[$.][ $_ - 1 ] eq"w";
    push @{ $l{"$. $_"} }, [ $_, $. - 1 ] if $b[ $. - 1 ][$_] eq " ";
    push @{ $l{"$. $_"} }, [ $_ - 1, $. ] if $b[$.][ $_ - 1 ] eq " ";
}
elsif($b[$.][$_]eq" "){
    push @{ $l{"@{[$.-1,$_]}"} }, [ $_, $. ] if $b[ $. - 1 ][$_] eq "w";
    push @{ $l{"@{[$.,$_-1]}"} }, [ $_, $. ] if $b[$.][ $_ - 1 ] eq "w";
}}}{print for sort map"@{$l{$_}[0]}",grep@{$l{$_}}==1,keys%l;
